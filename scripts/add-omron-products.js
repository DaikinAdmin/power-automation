// Simple categorization based on article ID patterns
function categorizeProduct(articleId) {
  const id = articleId.toUpperCase();
  
  if (id.startsWith('CP1L') || id.startsWith('CP2E') || id.startsWith('CPM2C')) {
    return {
      category: 'PLCs & Controllers',
      subcategory: 'Compact PLCs',
      type: 'plc'
    };
  }
  if (id.startsWith('CP1H') || id.startsWith('CP1E')) {
    return {
      category: 'PLCs & Controllers',
      subcategory: 'Modular PLCs',
      type: 'plc'
    };
  }
  if (id.startsWith('TBMA')) {
    return {
      category: 'PLCs & Controllers',
      subcategory: 'Terminal Blocks',
      type: 'terminal'
    };
  }
  if (id.startsWith('C200HW') || id.startsWith('CS1') || id.startsWith('CJ')) {
    if (id.includes('CPU')) {
      return {
        category: 'PLCs & Controllers',
        subcategory: 'PLC CPUs',
        type: 'cpu'
      };
    }
    if (id.includes('PA') || id.includes('PD')) {
      return {
        category: 'PLCs & Controllers',
        subcategory: 'PLC Power Supplies',
        type: 'power'
      };
    }
    if (id.includes('BC') || id.includes('BI')) {
      return {
        category: 'PLCs & Controllers',
        subcategory: 'Base Units',
        type: 'base'
      };
    }
    if (id.includes('W')) {
      return {
        category: 'PLCs & Controllers',
        subcategory: 'PLC I/O Modules',
        type: 'io'
      };
    }
    return {
      category: 'PLCs & Controllers',
      subcategory: 'PLC Accessories',
      type: 'accessory'
    };
  }
  if (id.startsWith('CV500CN')) {
    return {
      category: 'PLCs & Controllers',
      subcategory: 'Communication Modules',
      type: 'comm'
    };
  }
  if (id.startsWith('XW2Z') || id.startsWith('XW2D') || id.startsWith('XW2F') || id.startsWith('XW2R') || id.startsWith('XW2B') || id.startsWith('XW2G') || id.startsWith('XW2K')) {
    return {
      category: 'Connectors & Cables',
      subcategory: 'Industrial Connectors',
      type: 'connector'
    };
  }
  if (id.startsWith('SYSMAC') || id.startsWith('WS02') || id.startsWith('CX') || id.startsWith('SDKY') || id.startsWith('OPAL')) {
    return {
      category: 'Software & Licenses',
      subcategory: 'Programming Software',
      type: 'software'
    };
  }
  if (id.startsWith('WES')) {
    return {
      category: 'Network Equipment',
      subcategory: 'Industrial Ethernet Switches',
      type: 'network'
    };
  }
  if (id.startsWith('GW')) {
    return {
      category: 'Network Equipment',
      subcategory: 'Gateways',
      type: 'gateway'
    };
  }
  if (id.startsWith('KORJETWAVE')) {
    return {
      category: 'Network Equipment',
      subcategory: 'Wireless Modules',
      type: 'wireless'
    };
  }
  if (id.startsWith('W4S') || id.startsWith('HMCEF') || id.startsWith('SC4F')) {
    return {
      category: 'HMI & Displays',
      subcategory: 'Touch Panels',
      type: 'hmi'
    };
  }
  if (id.startsWith('G70A')) {
    return {
      category: 'Servo Systems',
      subcategory: 'Servo Drives',
      type: 'servo'
    };
  }
  if (id.startsWith('RS')) {
    return {
      category: 'Communication Modules',
      subcategory: 'Serial Communication',
      type: 'serial'
    };
  }
  if (id.startsWith('B')) {
    return {
      category: 'Sensors',
      subcategory: 'Photoelectric Sensors',
      type: 'sensor'
    };
  }
  if (id.startsWith('DCA')) {
    return {
      category: 'Power Supplies',
      subcategory: 'DIN Rail Power Supplies',
      type: 'power'
    };
  }
  if (id.startsWith('XS5') || id.startsWith('XS6')) {
    return {
      category: 'Sensors',
      subcategory: 'Proximity Sensors',
      type: 'sensor'
    };
  }
  if (id.startsWith('PLCKIT') || id.startsWith('NARTSM') || id.startsWith('SYSSCU')) {
    return {
      category: 'Development Kits',
      subcategory: 'Starter Kits',
      type: 'kit'
    };
  }
  
  // Default
  return {
    category: 'PLCs & Controllers',
    subcategory: 'PLC Accessories',
    type: 'accessory'
  };
}

